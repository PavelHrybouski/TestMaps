- (NSManagedObjectContext*) managedObjectContext {
    
    if (!_managedObjectContext) {
        _managedObjectContext = [[PHDataManger sharedManager] managedObjectContext];
    }
    return _managedObjectContext;
}
- (NSFetchedResultsController *)fetchedResultsController
{
    if (_fetchedResultsController != nil) {
        return _fetchedResultsController;
    }
    
    NSFetchRequest* fetchRequest = [[NSFetchRequest alloc] init];
    
    NSEntityDescription* description =
    [NSEntityDescription entityForName:@"PHLocations"
                inManagedObjectContext:self.managedObjectContext];
    
    [fetchRequest setEntity:description];
    

    NSSortDescriptor* dateDescriptor =
    [[NSSortDescriptor alloc] initWithKey:@"date" ascending:YES];
    
   
    [fetchRequest setSortDescriptors:@[dateDescriptor]];
    

    NSFetchedResultsController *aFetchedResultsController =
    [[NSFetchedResultsController alloc] initWithFetchRequest:fetchRequest
                                        managedObjectContext:self.managedObjectContext
                                          sectionNameKeyPath:nil
                                                   cacheName:nil];
    aFetchedResultsController.delegate = self;
    self.fetchedResultsController = aFetchedResultsController;
    
    NSError *error = nil;
    if (![self.fetchedResultsController performFetch:&error]) {
        // Replace this implementation with code to handle the error appropriately.
        // abort() causes the application to generate a crash log and terminate. You should not use this function in a shipping application, although it may be useful during development.
        NSLog(@"Unresolved error %@, %@", error, [error userInfo]);
        abort();
    }
    
    return _fetchedResultsController;
}








#pragma mark - UITableViewDelegate

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath {
    [tableView deselectRowAtIndexPath:indexPath animated:YES];
   
    PHMapAnnotation *annotation = [[PHMapAnnotation alloc] init];
    PHLocations *object = [self.fetchedResultsController objectAtIndexPath:indexPath];
    
    annotation.title = [NSString stringWithFormat:@"%@", object.latitude];
    annotation.subtitle = [NSString stringWithFormat:@"%@", object.longitude];
    CLLocationCoordinate2D coord;
    coord.longitude = (CLLocationDegrees)[object.longitude doubleValue];
    coord.latitude = (CLLocationDegrees)[object.latitude doubleValue];
    NSDictionary *dictionary = [[NSDictionary alloc] initWithObjectsAndKeys:object.longitude ,@"object.longitude",
    object.latitude, @"object.latitude" , nil];
    annotation.coordinate =coord;
    [[NSNotificationCenter defaultCenter] postNotificationName:@"ADDaPIN" object:nil userInfo:dictionary];
 

}






#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView
{
    return [[self.fetchedResultsController sections] count];
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    id <NSFetchedResultsSectionInfo> sectionInfo = [self.fetchedResultsController sections][section];
    return [sectionInfo numberOfObjects];
}
#pragma mark - Table View



- (StoryTableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString* identifier = @"Cell";
    
    StoryTableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:identifier];
    
    if (!cell) {
        cell = [[StoryTableViewCell alloc] initWithStyle:UITableViewCellStyleValue1 reuseIdentifier:identifier];
    }
    
    [self configureCell:cell atIndexPath:indexPath];
    
    return cell;
}


- (void)configureCell:(StoryTableViewCell *)cell atIndexPath:(NSIndexPath *)indexPath
{
    PHLocations *object = [self.fetchedResultsController objectAtIndexPath:indexPath];
    
    NSString *latitude = [NSString stringWithFormat:@"%@", object.latitude];
    NSString *longitude = [NSString stringWithFormat:@"%@", object.longitude];
    cell.latitudeLabel.text = latitude;
    cell.longitudeLabel.text = longitude;
    
    NSDateFormatter *newDateFormatter = [[NSDateFormatter alloc]init];
    newDateFormatter.locale = [[NSLocale alloc] initWithLocaleIdentifier:@"ru_RU"];
    
    [newDateFormatter setDateFormat:@"dd MMMM yyyy  HH:mm"];
    cell.dateLabel.text = [newDateFormatter stringFromDate:object.date];
    cell.cityLabel.text = object.city;
    
    cell.accessoryType = UITableViewCellAccessoryDisclosureIndicator;
    
}




